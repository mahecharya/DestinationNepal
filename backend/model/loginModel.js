import mongoose from "mongoose";

const loginSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    age:{
        type:Number,
        required:true
    },
    gender:{
        type:String,
        required:true
    }
},{timestamps:true}
)

const 
Login=mongoose.model("logindetais", loginSchema)

export default Login;